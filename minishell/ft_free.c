#include <stdlib.h>

void	ft_free(char ***str)
{
	int		i;

	i = 0;
	if (str && *str)
	{
		while ((*str)[i] != 0)
		{
			free((*str)[i]);
			(*str)[i] = NULL;
			++i;
		}
		free(*str);
		*str = NULL;
	}
}
